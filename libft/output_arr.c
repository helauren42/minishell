#include "libft.h"

void	output_arr(int *stack, int len)
{
	int	i;

	i = 0;
	while (i < len)
	{
		ft_putnbr_fd(stack[i], 1);
		write(1, " ", 1);
		i++;
	}
	write(1, "\n", 1);
}
